from __future__ import annotations

from dataclasses import dataclass
from typing import Literal

# Source type semantics:
# - "rss": `url` is a concrete RSS/Atom URL that your fetcher can read directly.
# - "rss_page": `url` is a page that lists RSS feeds; resolve the exact feed before fetching.
# - "public_page": `url` is a normal website/category page; do not scrape until ToS/robots/use rights are reviewed.
# - "generated": fallback source for AI-generated educational texts without an external article.
SourceType = Literal["rss", "rss_page", "public_page", "generated"]
Country = Literal["NL", "BE", "NL/BE", ""]
UseMode = Literal[
    "rss_summary",
    "source_facts",
    "facts_and_link_only",
    "full_text_if_allowed",
    "generated",
]


