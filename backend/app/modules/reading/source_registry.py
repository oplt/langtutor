def get_sources_for_interest(interest_area: str) -> list[SourceDefinition]:
    """Return a copy of configured sources for an interest area, falling back to news.

    Returning a copy prevents callers from accidentally mutating the global
    SOURCE_REGISTRY when they sort/shuffle/filter sources.
    """
    return list(SOURCE_REGISTRY.get(interest_area, SOURCE_REGISTRY["news"]))


def get_direct_rss_sources_for_interest(interest_area: str) -> list[SourceDefinition]:
    """Return only concrete RSS/Atom sources that can be fetched directly.

    rss_page and public_page entries are intentionally excluded; they need
    feed-resolution and/or Terms/robots review before automated crawling.
    """
    return [
        source
        for source in get_sources_for_interest(interest_area)
        if source.type == "rss" and source.rss_url and source.url == source.rss_url
    ]


def get_review_required_sources_for_interest(interest_area: str) -> list[SourceDefinition]:
    """Return sources requiring manual Terms/robots/licensing review before crawling."""
    return [
        source
        for source in get_sources_for_interest(interest_area)
        if source.commercial_use_check_required or source.type in {"rss_page", "public_page"}
    ]